    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(tfUsuario.getText().isEmpty() || pfSenha.getPassword().length == 0)
        {
            JOptionPane.showMessageDialog(this, "Não pode deixar algum campo em branco", "Alerta", 2);
        }
        else
        {
            if (tfUsuario.getText().equals(usuario) && Arrays.equals(pfSenha.getPassword(), senha))
            {
                FrmTelaPrincipal principal = new FrmTelaPrincipal();
                principal.setVisible(true);
                dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Usuario ou Senha Incorretos", "Alerta", 2);
            }
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

