    private void pfSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pfSenhaKeyPressed
        if (tfUsuario.getText().isEmpty()) 
        {
            JOptionPane.showMessageDialog(this, "Favor informar o usuário!", "Erro!", JOptionPane.ERROR_MESSAGE);
            pfSenha.setText("");
            pfSenha.setFocusable(false);
            tfUsuario.setFocusable(true);
            pfSenha.setFocusable(true);
        } else 
        {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) 
            {
                if (tfUsuario.getText().equals(usuario) && Arrays.equals(pfSenha.getPassword(), senha))
                {
                    FrmTelaPrincipal principal = new FrmTelaPrincipal();
                    principal.setVisible(true);
                    dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Usuario ou Senha Incorretos", "Alerta", 2);
                }
            }
        }
    }//GEN-LAST:event_pfSenhaKeyPressed

